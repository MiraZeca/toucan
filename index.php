          <h2 class="text-center naslov" style="text-transform:uppercase">Upoznajte pticu tukan</h2>
          <p>&diams; Tukani žive na tropskim i egzotičnim lokacijama <a href="https://en.wikipedia.org/wiki/File:South_America_(orthographic_projection).svg" target="_blank" class="link">Južne Amerike</a>, ali uglavnom žive u predelu <a href="https://en.wikipedia.org/wiki/Brazil#/media/File:BRA_orthographic.svg" target="_blank" class="link">Brazila</a>, <a href="https://en.wikipedia.org/wiki/Paraguay#/media/File:PRY_orthographic.svg" target="_blank" class="link">Paragvaja</a>, <a href="https://en.wikipedia.org/wiki/Argentina#/media/File:ARG_orthographic_(+all_claims).svg" target="_blank" class="link">Argentine</a> i <a href="https://en.wikipedia.org/wiki/Bolivia#/media/File:BOL_orthographic.svg" target="_blank" class="link">Bolivije</a>.</p>
          <p>&diams; Tukan je svaštojed, što znači da jedu i insekte i voće. Neki od insekata koji se nalaze u njihovoj ishrani su cvrčci i pauci. Neki od plodova koje jedu su banane i razne vrste bobica. Neke druge stvari koje se sastoje u njihovoj ishrani su glodari, gmizavci i slabije ptice.</p>
          <p>&diams; Tukan takođe ima predatore. Glavni su ljudi, velike mačke, najgori je jaguar i veće ptice.</p>
          <p>&diams; Tukani su vrlo razigrane ptice.</p>
          <p>&diams; Postoji oko 40 vrsta tukana. Najveći, toco toucan, prikazan je na kutijama za žitarice <a href="https://www.kelloggs.com.au/en_AU/products/froot-loops-product.html" target="_blank" class="link">Fruit Loops</a>.</p>
          <p>&diams; Zabavno je gledati kako tukan koristi svoj kljun za jelo. Prvo zgrabi hranu na vrhu kljuna. Zatim, tukan zabacujući glavu baca hranu u grlo. Dug čekinjast jezik pomaže pri pomeranju hrane nadole.</p>
          <p>&diams; Skloni su da više skaču nego što lete. Njihove svetle boje pružaju dobru kamuflažu u sjajnoj svetlosti krošnje prašume.</p>
          <p>&diams; Boja kljuna može biti crna, plava, braon, zelena, crvena, bela, žuta ili u kombinaciji boja. Svetla boja na kljunu pomaže u privlačenju partnera tukana.</p>
          <p>&diams; Tukani nisu veliki letači pa imaju tendenciju da skaču dugačke grane i radije klize kad god je to moguće.</p>

          <p class="text-center">&diams; Dužina: 46 – 56 cm.&diams;</p>
          <p class="text-center">&diams; Težina: 285 – 455 g. &diams;</p>
